# 6. Даний рядок. Отримайте новий рядок, вставивши між кожними двома символами вихідної рядки символ *. Виведіть отриманий рядок.

string = input("「Введите строку」: ")
str2 = ''
for i in range(0, len(string)):
    if i % 2 == 0:
        str2 += string[i]+'*'
    else:
        str2 += string[i]
print("【Результат】 => ", str2)
