# 3. Дано два ненульових числа. Знайти суму, різницю, добуток і частку їх квадратів.

a = float(input("a = "))
b = float(input("b = "))
a != 0 and b != 0
print("(a^2 + b^2) =", a ** 2 + b ** 2)
print('(a^2 - b^2) =', a ** 2 - b ** 2)
print("(a^2 * b^2) =", a ** 2 * b ** 2)
print("(a^2 / b^2) =", a ** 2 / b ** 2)
