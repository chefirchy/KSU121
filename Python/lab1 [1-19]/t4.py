# 4. Дано катети прямокутного трикутника a і b. Знайти його гіпотенузу c і периметр P: c=(a^2+b^2)^1/2, P=a+b+c.

import math
a = float(input("a = "))
b = float(input("b = "))
a >= 0 and b >= 0
c = math.sqrt(a ** 2 + b ** 2)
P = a + b + c
print("c[гипотенуза] =", c)
print('P[периметр] =', P)
