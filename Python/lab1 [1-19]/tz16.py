# 16. Відомо, що X кг цукерок коштують A гривень. Визначити, скільки коштує 1 кг і Y кг цих же цукерок.

print("Умова: Відомо, що X кг цукерок коштують A гривень. Визначити, скільки коштує 1 кг і Y кг цих же цукерок.")
x, a, y = map(int, input("Значення「X」(кг), 「A」(грн), 「Y」(кг): ").split())
print("【1 кг】 = {0}[грн], 【{2}】[кг] = {1}[грн]".format(a / x, y*(a/x), y))
